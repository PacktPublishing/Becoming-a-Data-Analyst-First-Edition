CREATE DATABASE ECommerceHub;

USE DATABASE ECommerceHub;

CREATE TABLE product_category (
    category_id INT AUTO_INCREMENT PRIMARY KEY,
    category_name VARCHAR(100) NOT NULL
);

CREATE TABLE product (
    product_id INT AUTO_INCREMENT PRIMARY KEY,
    product_name VARCHAR(100) NOT NULL,
    description TEXT,
    product_price DECIMAL(10,2) NOT NULL,
    is_on_discount BOOLEAN DEFAULT FALSE,
    discount_percentage DECIMAL(3,2) DEFAULT 0.00,
    category_id_fk INT,
    units_in_stock INT,
    discounted_price DECIMAL(3,2) DEFAULT product_price
    FOREIGN KEY (category_id_fk) REFERENCES product_category(category_id)
);

CREATE TABLE customer (
    customer_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(100) NOT NULL,
    last_name VARCHAR(100) NOT NULL,
    username VARCHAR(100) NOT NULL UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    phone_number VARCHAR(20)
);

CREATE TABLE orders (
orders_id INT AUTO_INCREMENT PRIMARY KEY,
customer_id_fk INT,
order_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
payment_method_id_fk INT,
unit_cost DECIMAL(10,2),
quantity INT,
product_id_fk INT,
total_cost DECIMAL(10,2),
FOREIGN KEY(customer_id_fk) REFERENCES customer(customer_id),
FOREIGN KEY(payment_method_id_fk) REFERENCES payment_method(payment_method_id)
FOREIGN KEY(product_id_fk) REFERENCES product(product_id)
);

CREATE TABLE payment_method(
payment_method_id INT AUTO_INCREMENT PRIMARY KEY,
payment_method_name VARCHAR(50) NOT NULL
);

CREATE TABLE shipping_information (
shipping_id INT AUTO_INCREMENT PRIMARY KEY,
orders_id_fk INT,
receiver_name VARCHAR(100),
address VARCHAR(255) NOT NULL,
city VARCHAR(50) NOT NULL,
state VARCHAR(50),
country VARCHAR(50) NOT NULL,
postal_code VARCHAR(10),
phone_number VARCHAR(20), 
expected_delivery TIMESTAMP,
actual_delivery TIMESTAMP,
FOREIGN KEY(orders_id_fk) REFERENCES orders(orders_id)
);

CREATE TABLE product_reviews (
    review_id INT AUTO_INCREMENT PRIMARY KEY,
    product_id_fk INT,
    customer_id_fk INT,
    rating INT NOT NULL,
    review TEXT,
    review_date DATE NOT NULL,
    FOREIGN KEY (product_id_fk) REFERENCES product(product_id),
    FOREIGN KEY (customer_id_fk) REFERENCES customer(customer_id)
);
